// routes/symptoms.js
const express = require('express');
const router = express.Router();
const Symptom = require('../models/Symptom');

// Get all symptoms
router.get('/', async (req, res) => {
  try {
    const { bodyPart, search } = req.query;
    let query = {};

    if (bodyPart) query.bodyPart = bodyPart;
    if (search) query.name = { $regex: search, $options: 'i' };

    const symptoms = await Symptom.find(query).populate('relatedDiseases');
    res.json({ success: true, count: symptoms.length, data: symptoms });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
});

// Get symptom by ID
router.get('/:id', async (req, res) => {
  try {
    const symptom = await Symptom.findById(req.params.id).populate('relatedDiseases');
    if (!symptom) {
      return res.status(404).json({ success: false, message: 'Symptom not found' });
    }
    res.json({ success: true, data: symptom });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
});

// Create symptom (admin)
router.post('/', async (req, res) => {
  try {
    const symptom = await Symptom.create(req.body);
    res.status(201).json({ success: true, data: symptom });
  } catch (error) {
    res.status(400).json({ success: false, message: error.message });
  }
});

module.exports = router;
